#include "object_bar.h"

object_bar::object_bar(const glm::vec3& position, const glm::vec3& light_position, material* material)
	:object(position, light_position, material)
{
	init_shape();
	object::init_buffers();
}

object_bar::object_bar(const glm::vec3& position, const glm::vec3& light_position, material* material,
	const char* texture_path): object(position, light_position, material)
{
	init_shape();
	set_texture(texture_path);
	object::init_buffers();
}

